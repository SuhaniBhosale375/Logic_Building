#include<stdio.h>

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//  Function name :    FactRev 
//  Discription   :    Display reverse factor 
//  Input         :    Integer
//  Output        :    Integers
//  Author        :    Suhani Rajendra Bhosale 
//  Data          :    23/01/2024
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////


void FactRev(int iNo)
{
    int iCnt = 0;

    for(iCnt = (iNo/2) ; iCnt >= 1; iCnt--)
    {
        if((iNo % iCnt) == 0)
        {
            printf("%d\t",iCnt);
        }
    }

}

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//  Entry point of the given function
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;
   
    printf("Enter number\n");
    scanf("%d",&iValue);

    FactRev(iValue);

    return 0;
}

