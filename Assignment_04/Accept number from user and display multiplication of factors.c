///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//  Problem Statement : Write a program which accept number from user and display its multiplication of factors
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

#include<stdio.h>

int MultFactor(int iNo)
{
    int iMult = 1;
    int iCnt = 0;

    for(iCnt = 1 ; iCnt <= (iNo/2); iCnt++)
    {
        if((iNo % iCnt) == 0)
        {
            iMult = iMult * iCnt;
        }
    }

    return iMult;
}

int main()
{
    int iValue = 0;
    int iRet = 0;

    printf("Enter number\n");
    scanf("%d",&iValue);

    iRet = MultFactor(iValue);

    printf("Multiplication of factors %d : %d",iValue,iRet);

    return 0;
}

