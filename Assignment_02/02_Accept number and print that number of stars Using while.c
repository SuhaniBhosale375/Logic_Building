//////////////////////////////////////////////////////////////////////////////////////////
// Problem statement : Accept one number from user print that number of * on screen
//////////////////////////////////////////////////////////////////////////////////////////

# include<stdio.h>

void Display(int iNo)
{
    while(iNo > 0 )
    {
        printf("*");
        iNo--;
    }
    
}

int main()
{
    int iValue = 0;

    printf("Enter any number : ");
    scanf("%d",&iValue);

    Display(iValue);

    return 0;
}
