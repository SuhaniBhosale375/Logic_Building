////////////////////////////////////////////////////////////////////////////////////////////////////////

#include<stdio.h>

void RevFact(int iNo)
{
    int iCnt = 0;

    if(iNo < 0)      // Updater
    {
        iNo = -iNo;
    }
    for(iCnt = (iNo/2); iCnt >= 1;  iCnt--)
    {
        if((iNo % iCnt) == 0)
        {
           printf("%d\t",iCnt);
        }
    }
}

int main()
{
    int iValue = 0;

    printf("Enter nmber\n ");
    scanf("%d",&iValue);

    RevFact(iValue);

}

