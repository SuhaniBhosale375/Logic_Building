////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//  Problem statement : Program to print 5 times "Marvellous" on the screen.
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

#include<stdio.h>

//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//  Function name :     Display
//  Discription   :     Display 5 times Marvellous
//  Input         :     integer
//  Output        :     string
//  Author        :     Suhani Rajendra Bhosale
//  Date          :     20/01/2024 
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

void Display()
{
    int iCnt = 0;

    for(iCnt = 1; iCnt <= 5; iCnt++)
    {
        printf("Marvellous\n");
    }
}
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//  Entry point of the application
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    Display();

    return 0;
}

