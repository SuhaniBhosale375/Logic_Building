// Problem statement : Accept one number and check weathe is divisible by 5 or not
//////////////////////////////////////////////////////////////////////////////////////////

//////////////////////////////////////////////////////////////////////////////////////////
//    Algorithm
//////////////////////////////////////////////////////////////////////////////////////////
/*
    START
        Accept one number from user
        Divide that number and check weather the number is divisile bye 5 or not
        if the remainder is zero
            then Display number is divisible by 5
        Otherwise
            Display number is not divisible by 5
        

    END

*/
/////////////////////////////////////////////////////////////////////////////////////////

# include<stdio.h>

typedef int bool;

#define TRUE 1
#define FALSE 0

/////////////////////////////////////////////////////////////////////////////////////////
//  Function Name :     Check
//  Discription   :     divisible by 5 or not
//  Input         :     integer
//  Output        :     boolean
//  Author        :     Suhani Rajendra Bhosale
//  Date          :     20/01/2024
/////////////////////////////////////////////////////////////////////////////////////////

bool Check(int iNo)
{
    if((iNo % 5) == 0)
    {
        return TRUE;
    }
    else
    {
        return FALSE;
    }
}

/////////////////////////////////////////////////////////////////////////////////////////
//  Entry point of the application
/////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;
    int iRet = FALSE;

    printf("Enter any number : ");
    scanf("%d",&iValue);

    iRet = Check(iValue);

    if(iRet == TRUE)
    {
        printf("Divisible by 5\n");
    }
    else
    {
        printf(" NOT Divisible by 5\n");
    }
    
    return 0;


}

