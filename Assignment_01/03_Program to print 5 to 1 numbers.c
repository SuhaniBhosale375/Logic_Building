/////////////////////////////////////////////////////////////////////////////////////////
//  Problem Statement : Program to print 5 to 1 numbers on screen
/////////////////////////////////////////////////////////////////////////////////////////


/////////////////////////////////////////////////////////////////////////////////////////

#include<stdio.h>

/////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name :     Display
//  Discription   :     Display the reverse number 
//  Input         :     integer
//  Output        :     integer
//  Author        :     Suhani Rajendra Bhosale
//  Data          :     20/01/2024
//
/////////////////////////////////////////////////////////////////////////////////////////

void Display()
{
    int i = 0;
   
    for(i = 5; i >= 1; i--)
    {
        printf("%d\t",i);
        
    }
}
/////////////////////////////////////////////////////////////////////////////////////////
//  Entry point of the application
/////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    Display();
   
    return 0;
}

